use strict;
use warnings;

my $num3;
my $num1 = 10;
my $num2 = 5;

=pod
    Numeric Operators:
        (+) - Addition
        (-) - Subtraction
        (*) - Multiplication
        (/) - Division
=cut

print("Addition:\n");
$num3 = $num1 + $num2;
print "$num1 + $num2 = $num3\n";

print("Subtraction:\n");
$num3 = $num1 - $num2;
print "$num1 - $num2 = $num3\n";

print("Multiplication:\n");
$num3 = $num1 * $num2;
print "$num1 * $num2 = $num3\n";

print("Division: \n");
$num3 = $num1 / $num2;
print "$num1 / $num2 = $num3\n";